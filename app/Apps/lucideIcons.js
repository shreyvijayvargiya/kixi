// Get all available lucide icons as a list
export const getAllLucideIcons = () => {
	return lucideIconNames
		.filter((iconName) => {
			try {
				return (
					LucideIcons[iconName] && typeof LucideIcons[iconName] === "function"
				);
			} catch {
				return false;
			}
		})
		.map((iconName) => ({
			name: iconName,
			component: LucideIcons[iconName],
		}))
		.sort((a, b) => a.name.localeCompare(b.name));
};

